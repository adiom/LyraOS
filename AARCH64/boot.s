.section .text
.global _start

_start:
    ldr x0, =0x3F00   // Загрузка адреса в регистр x0
    ldr x1, =msg      // Загрузка адреса сообщения в регистр x1
    mov x2, #0        // Обнуление регистра x2

.loop:
    ldrb w3, [x1, x2] // Загрузка байта из сообщения
    cbz w3, .done     // Если байт равен 0, переход к .done
    strb w3, [x0], #1 // Запись байта в адрес x0 и инкрементирование x0
    add x2, x2, #1    // Увеличение x2
    b .loop           // Переход к началу цикла

.done:
    ret                // Возврат (если это не конечный код, то используйте бесконечный цикл)
    
.section .rodata
msg:
    .asciz "Hello, World!\n" // Сообщение
